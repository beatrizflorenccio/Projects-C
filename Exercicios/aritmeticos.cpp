#include <iostream> 

using namespace std;

int main() {
    float x = 9.0;
    float y = 4.0;

    cout << "Os valores de x e y são, respectivamente, " << x << " e " << y << endl;
    cout << "A soma entre eles vale: " << x + y << endl;
    cout << "A subtração entre eles vale: " << x - y << endl;
    cout << "A multiplicação entre eles vale: " << x * y << endl;

    float z = x / y;
    cout << "A divisão entre eles vale: " << z << endl;

    return 0;

}